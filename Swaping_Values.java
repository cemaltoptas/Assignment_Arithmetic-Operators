package assignment3;

public class Swaping_Values {

	public static void main(String[] args) {
		// Swap the given values
		
		int Num1 = 10;
		int Num2 = 20;
		
		System.out.println("n1= " + Num1);
		System.out.println("n2= " + Num2);
		
		 int Temp = Num1;
		 Num1 = Num2;
		 Num2 = Temp;
		 System.out.println("-------------");
		 
		 System.out.println("n1= " + Num1);
		 System.out.println("n2= " + Num2);
		 
	}

}
